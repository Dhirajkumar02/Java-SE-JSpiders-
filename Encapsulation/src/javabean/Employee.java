package javabean;

public class Employee {
	private int id;
	private String name;
	private double sal;

	public void setId(int id) {
		this.id=id;
	}
	public int getId() {
		return id;
	}

	public void setName(String name) {
		this.name=name;
	}

	public String getName() {
		return name;
	}

	public void setId(double sal) {
		this.sal=sal;
	}

	public double getSal() {
		return sal;
	}


}
